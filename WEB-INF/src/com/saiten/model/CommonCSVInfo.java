package com.saiten.model;

/**
 * The Class CommonCSVInfo.
 *
 * @author suwarna
 * @version $Revision: 1.0 $
 */
public class CommonCSVInfo {

	/** The subject code. */
	private String subjectCode;

	/** The school code. */
	private String schoolCode;

	/** The class num. */
	private String classNum;

	/** The box num. */
	private String boxNum;

	/** The position. */
	private Short position;

	/** The summary flag. */
	private String summaryFlag;

	/** The line number. */
	private Integer lineNumber;

	/**
	 * Instantiates a new common csv info.
	 */
	public CommonCSVInfo() {
	}

	/**
	 * Gets the subject code.
	 *
	 * 
	 * @return the subject code
	 */
	public String getSubjectCode() {
		return subjectCode;
	}

	/**
	 * Sets the subject code.
	 *
	 * @param subjectCode
	 *            the new subject code
	 */
	public void setSubjectCode(String subjectCode) {
		this.subjectCode = subjectCode;
	}

	/**
	 * Gets the school code.
	 *
	 * 
	 * @return the school code
	 */
	public String getSchoolCode() {
		return schoolCode;
	}

	/**
	 * Sets the school code.
	 *
	 * @param schoolCode
	 *            the new school code
	 */
	public void setSchoolCode(String schoolCode) {
		this.schoolCode = schoolCode;
	}

	/**
	 * Gets the class num.
	 *
	 * 
	 * @return the class num
	 */
	public String getClassNum() {
		return classNum;
	}

	/**
	 * Sets the class num.
	 *
	 * @param classNum
	 *            the new class num
	 */
	public void setClassNum(String classNum) {
		this.classNum = classNum;
	}

	/**
	 * Gets the box num.
	 *
	 * 
	 * @return the box num
	 */
	public String getBoxNum() {
		return boxNum;
	}

	/**
	 * Sets the box num.
	 *
	 * @param boxNum
	 *            the new box num
	 */
	public void setBoxNum(String boxNum) {
		this.boxNum = boxNum;
	}

	/**
	 * Gets the position.
	 *
	 * 
	 * @return the position
	 */
	public Short getPosition() {
		return position;
	}

	/**
	 * Sets the position.
	 *
	 * @param position
	 *            the new position
	 */
	public void setPosition(Short position) {
		this.position = position;
	}

	/**
	 * Gets the summary flag.
	 *
	 * 
	 * @return the summary flag
	 */
	public String getSummaryFlag() {
		return summaryFlag;
	}

	/**
	 * Sets the summary flag.
	 *
	 * @param summaryFlag
	 *            the new summary flag
	 */
	public void setSummaryFlag(String summaryFlag) {
		this.summaryFlag = summaryFlag;
	}

	/**
	 * Gets the line number.
	 *
	 * 
	 * @return the line number
	 */
	public Integer getLineNumber() {
		return lineNumber;
	}

	/**
	 * Sets the line number.
	 *
	 * @param lineNumber
	 *            the new line number
	 */
	public void setLineNumber(Integer lineNumber) {
		this.lineNumber = lineNumber;
	}

}
